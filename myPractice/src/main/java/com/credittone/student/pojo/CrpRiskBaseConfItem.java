package com.credittone.student.pojo;


public class CrpRiskBaseConfItem {

  private long riskBaseConfId;
  private String riskBaseConfName;
  private String riskBaseConfNo;
  private String riskBaseConfMap;
  private String isContinuous;
  private String riskEnumKey;
  private String riskEnumName;


  public long getRiskBaseConfId() {
    return riskBaseConfId;
  }

  public void setRiskBaseConfId(long riskBaseConfId) {
    this.riskBaseConfId = riskBaseConfId;
  }


  public String getRiskBaseConfName() {
    return riskBaseConfName;
  }

  public void setRiskBaseConfName(String riskBaseConfName) {
    this.riskBaseConfName = riskBaseConfName;
  }


  public String getRiskBaseConfNo() {
    return riskBaseConfNo;
  }

  public void setRiskBaseConfNo(String riskBaseConfNo) {
    this.riskBaseConfNo = riskBaseConfNo;
  }


  public String getRiskBaseConfMap() {
    return riskBaseConfMap;
  }

  public void setRiskBaseConfMap(String riskBaseConfMap) {
    this.riskBaseConfMap = riskBaseConfMap;
  }


  public String getIsContinuous() {
    return isContinuous;
  }

  public void setIsContinuous(String isContinuous) {
    this.isContinuous = isContinuous;
  }


  public String getRiskEnumKey() {
    return riskEnumKey;
  }

  public void setRiskEnumKey(String riskEnumKey) {
    this.riskEnumKey = riskEnumKey;
  }


  public String getRiskEnumName() {
    return riskEnumName;
  }

  public void setRiskEnumName(String riskEnumName) {
    this.riskEnumName = riskEnumName;
  }

}
